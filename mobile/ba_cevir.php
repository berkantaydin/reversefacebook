function url_title($str, $separator = 'dash', $lowercase = FALSE) {
    if ($separator == 'dash') {
        $search = '_';
        $replace = '-';
    } else {
        $search = '-';
        $replace = '_';
    }

    $trans = array(
        '&\#\d+?;' => '',
        '&\S+?;' => '',
        '\s+' => $replace,
        '[^a-z0-9\-\._]' => '',
        $replace . '+' => $replace,
        $replace . '$' => $replace,
        '^' . $replace => $replace,
        '\.+$' => ''
    );

    $str = strip_tags($str);

    foreach ($trans as $key => $val) {
        $str = preg_replace("#" . $key . "#i", $val, $str);
    }

    if ($lowercase === TRUE) {
        $str = strtolower($str);
    }

    return trim(stripslashes($str));
}

function ba_cevir($str) {
    return convert_accented_characters(url_title($str));
}

function ba_cevir_url($id = '', $name = '') {
    $pageurl = "/mobile/get.php?id=";
    $pageurl .= genid_from_id($id);
    $pageurl .= '&' . ba_cevir($name);

    return $pageurl;
}

